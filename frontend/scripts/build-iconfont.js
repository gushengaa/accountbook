/**
 * 裁剪 iconfont 仅保留项目用到的字形，生成精简版 iconUnicode.js + iconfontBase64.js
 * 用法: node scripts/build-iconfont.js
 */
const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');

const ROOT = path.join(__dirname, '..');
const CSS_PATH = path.join(ROOT, 'assets/iconfont/iconfont.css');
const TTF_PATH = path.join(ROOT, 'assets/iconfont/iconfont.ttf');
const ICON_MAP_PATH = path.join(ROOT, 'utils/iconMap.js');
const OUT_UNICODE = path.join(ROOT, 'utils/iconUnicode.js');
const OUT_BASE64 = path.join(ROOT, 'utils/iconfontBase64.js');
const TMP_PKG = path.join(ROOT, '_tmp_pkg');

function collectUsedBases() {
  const src = fs.readFileSync(ICON_MAP_PATH, 'utf8');
  const bases = new Set(['file-text']);

  const valueRe = /:\s*'([^']+)'/g;
  let m;
  while ((m = valueRe.exec(src))) {
    const v = m[1];
    if (v.startsWith('ri-') || v.includes('#') || v.includes('👶') || /[\u4e00-\u9fff]/.test(v)) continue;
    if (/^[a-z0-9-]+$/.test(v)) bases.add(v);
  }

  // 分类/后台可能配置的 ri- 类名
  bases.add('home');
  bases.add('wallet');
  bases.add('user');
  bases.add('edit');
  bases.add('add');
  bases.add('bar-chart');
  bases.add('file-list');
  bases.add('settings');
  bases.add('check');
  bases.add('arrow-down-s');
  bases.add('share');
  bases.add('delete-bin');
  bases.add('close');
  bases.add('image');
  bases.add('camera');
  bases.add('calendar');
  bases.add('time');
  bases.add('eye');
  bases.add('eye-off');

  return bases;
}

function buildClassSet(bases) {
  const classes = new Set();
  for (const base of bases) {
    classes.add(`ri-${base}-line`);
    classes.add(`ri-${base}-fill`);
  }
  return classes;
}

function parseCssMap(css, classes) {
  const map = {};
  const re = /\.(ri-[\w-]+):before\s*\{\s*content:\s*"\\([0-9a-fA-F]+)"/g;
  let match;
  while ((match = re.exec(css))) {
    if (!classes.has(match[1])) continue;
    map[match[1]] = String.fromCharCode(parseInt(match[2], 16));
  }
  return map;
}

async function subsetFont(text) {
  if (!fs.existsSync(TMP_PKG)) fs.mkdirSync(TMP_PKG, { recursive: true });
  const tgz = path.join(TMP_PKG, 'subset-font-2.1.0.tgz');
  if (!fs.existsSync(tgz)) {
    execSync('npm pack subset-font@2.1.0', { cwd: TMP_PKG, stdio: 'inherit' });
  }
  const extractDir = path.join(TMP_PKG, 'subset-font');
  if (!fs.existsSync(path.join(extractDir, 'package.json'))) {
    fs.mkdirSync(extractDir, { recursive: true });
    execSync(`tar -xzf "${tgz}" -C "${extractDir}"`, { stdio: 'inherit' });
  }
  const subsetFontFn = require(path.join(extractDir, 'package/index.js'));

  const fontBuffer = fs.readFileSync(TTF_PATH);
  const out = await subsetFontFn(fontBuffer, text, { targetFormat: 'truetype' });
  return Buffer.from(out);
}

async function main() {
  if (!fs.existsSync(TTF_PATH)) {
    console.error('Missing', TTF_PATH, '- run: node scripts/ensure-iconfont.js');
    process.exit(1);
  }

  const css = fs.readFileSync(CSS_PATH, 'utf8');
  const bases = collectUsedBases();
  const classes = buildClassSet(bases);
  const map = parseCssMap(css, classes);

  if (Object.keys(map).length < 10) {
    console.error('Too few icons parsed, abort');
    process.exit(1);
  }

  const text = Object.values(map).join('');
  console.log('Icons:', Object.keys(map).length, 'chars:', text.length);

  const subsetBuffer = await subsetFont(text);
  const b64 = subsetBuffer.toString('base64');

  fs.writeFileSync(
    OUT_UNICODE,
    `// Auto-generated by scripts/build-iconfont.js (${Object.keys(map).length} icons)\nexport const ICON_UNICODE = ${JSON.stringify(map)};\n`
  );
  fs.writeFileSync(
    OUT_BASE64,
    `// Auto-generated by scripts/build-iconfont.js (${Math.round(subsetBuffer.length / 1024)}KB)\nexport const ICONFONT_BASE64 = ${JSON.stringify(b64)};\n`
  );

  console.log('subset ttf:', Math.round(subsetBuffer.length / 1024) + 'KB');
  console.log('base64 js:', Math.round(fs.statSync(OUT_BASE64).size / 1024) + 'KB');
  console.log('iconUnicode.js:', Math.round(fs.statSync(OUT_UNICODE).size / 1024) + 'KB');
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
